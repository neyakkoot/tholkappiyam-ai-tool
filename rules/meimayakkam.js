const MeimayakkamRules = {
    check: function(word) {
        // மெய்ம்மயக்கத்தைச் சோதிக்கச் சொல்லின் நடுவில் உள்ள ஒற்றுகளைப் பார்க்க வேண்டும்.
        
        // விதி 1: 'ல்' முன் 'ம்' வருதல் கூடாது (நூன்மரபு 23/24)
        // *கல்மம், *பல்மணி
        if (/ல்[மமிமுமெமேமைமொமோமௌ]/.test(word)) {
             // புணர்ச்சியாக இருந்தால் (கல் + மனம்) ஏற்கலாம், ஆனால் தனிச்சொல்லாக (intra-word) கூடாது.
             // எளிய முன்மாதிரிக்காக இதைப் பிழையாகக் காட்டுவோம்.
             return {
                 isValid: false,
                 reason: "நூன்மரபு நூற்பா 23: 'ல்' (l) என்னும் லகர ஒற்றின் முன் 'ம்' (m) மயங்காது. (க, ச, ப மட்டுமே வரும்)."
             };
        }

        // விதி 2: 'ட்', 'ற்' முன் க, ச, ப மட்டுமே வரும் (வேற்றுநிலை)
        // *காட்மா (தவறு) - ட் முன் ம வராது
        if (/ட்[ஞநமனயரலவழள]/.test(word)) {
            return {
                isValid: false,
                reason: "தொகைமரபு: 'ட்' ஒற்றின் முன் வல்லினம் (க,ச,ப) மட்டுமே மிகும். பிற மெய்கள் மயங்காது."
            };
        }

        // விதி 3: னகர ஒற்றின் முன் 'ர'கரம் வருதல் (நூன்மரபு)
        // *நன்ரி (தவறு)
        if (/ன்[ரரிரு]/.test(word)) {
            return {
                isValid: false,
                reason: "நூன்மரபு: 'ன்' (Alveolar n) ஒற்றின் முன் 'ர' (r) மயங்காது. அதன் இனமான 'ற்' (R) மட்டுமே வரும் (எ.கா: நன்றி)."
            };
        }

        return { isValid: true };
    }
};
