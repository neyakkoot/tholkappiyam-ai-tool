const MozhimuthalRules = {
    check: function(word) {
        // விதி 1: 'வ'கரம் 'உ, ஊ, ஒ, ஓ' உடன் வராது (மொழிமரபு 30)
        // வுட்கை, வோட்கை போன்றவை பிழை
        if (/^வ[ுூொோௌ]/.test(word)) {
            return {
                isValid: false,
                reason: "மொழிமரபு நூற்பா 30: 'வ'கரம் உ, ஊ, ஒ, ஓ ஆகிய உயிர்களுடன் மொழிக்கு முதலில் வராது."
            };
        }

        // விதி 2: ட, ற, ல, ள, ழ, ண ஆகியவை மொழிக்கு முதலில் வராது (நூன்மரபு)
        // டமாரம் (Damaram), றக்கை (Rakkai) போன்றவை பிழை
        if (/^[டறலளழண]/.test(word)) {
            return {
                isValid: false,
                reason: "நூன்மரபு: ட, ற, ல, ள, ழ, ண ஆகிய மெய்கள் மொழிக்கு முதலில் வராது."
            };
        }

        return { isValid: true };
    }
};
