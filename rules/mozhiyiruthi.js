const MozhiyiruthiRules = {
    check: function(word) {
        // சொல்லின் கடைசி எழுத்தை எடுத்தல்
        const lastChar = word.slice(-1);

        // வல்லின மெய்கள் (க், ச், த், ப், ட், ற்) இறுதியில் வரக்கூடாது
        // மெய் வடிவம் (புள்ளி வைத்த எழுத்து) இருப்பின் பிழை
        if (/[க்ஞ்ட்ண்த்ந்ப்ம்ய்ர்ல்வ்ழ்ள்ற்ன்]/.test(lastChar)) {
             // வல்லினம் இறுதியில் வருகிறதா எனச் சோதித்தல்
             if (/[க்ஞ்ட்ண்த்ந்ப்]/.test(lastChar)) { // குறிப்பு: ட், ற் போன்றவற்றையும் சேர்க்கலாம்
                 return {
                     isValid: false,
                     reason: "மொழிமரபு: வல்லின மெய்கள் (க், ச், த், ப்) மொழிக்கு இறுதியில் வராது. குற்றியலுகரமாகவே வரும்."
                 };
             }
             // விதிவிலக்கு: உசு, முசு, தபு (மொழிமரபு)
        }
        
        return { isValid: true };
    }
};
